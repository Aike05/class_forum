-- 创建数据库
CREATE DATABASE IF NOT EXISTS `class_forum`
    DEFAULT CHARACTER SET utf8mb4
    COLLATE utf8mb4_unicode_ci;

USE `class_forum`;

-- ============================================
-- 1. 用户表 (users)
-- ============================================
CREATE TABLE IF NOT EXISTS `users` (
    `id` INT AUTO_INCREMENT PRIMARY KEY,
    `username` VARCHAR(50) NOT NULL UNIQUE COMMENT '登录用户名',
    `password` VARCHAR(255) NOT NULL COMMENT '密码（bcrypt哈希）',
    `nickname` VARCHAR(50) NOT NULL COMMENT '显示昵称',
    `email` VARCHAR(100) DEFAULT '' COMMENT '邮箱',
    `avatar` VARCHAR(255) DEFAULT '' COMMENT '头像路径',
    `role` ENUM('user', 'admin') DEFAULT 'user' COMMENT '角色：user=普通用户, admin=管理员',
    `created_at` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '注册时间',
    INDEX `idx_role` (`role`),
    INDEX `idx_created` (`created_at`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户表';

-- ============================================
-- 2. 帖子表 (posts)
-- ============================================
CREATE TABLE IF NOT EXISTS `posts` (
    `id` INT AUTO_INCREMENT PRIMARY KEY,
    `title` VARCHAR(200) NOT NULL COMMENT '帖子标题',
    `content` TEXT NOT NULL COMMENT '帖子内容',
    `user_id` INT NOT NULL COMMENT '发布者ID',
    `category` VARCHAR(50) DEFAULT '综合讨论' COMMENT '分类',
    `views` INT DEFAULT 0 COMMENT '浏览量',
    `created_at` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '发布时间',
    `updated_at` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    INDEX `idx_user` (`user_id`),
    INDEX `idx_category` (`category`),
    INDEX `idx_views` (`views`),
    INDEX `idx_created` (`created_at`),
    CONSTRAINT `fk_posts_user` FOREIGN KEY (`user_id`) REFERENCES `users`(`id`) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='帖子表';

-- ============================================
-- 3. 留言表 (messages)
-- ============================================
CREATE TABLE IF NOT EXISTS `messages` (
    `id` INT AUTO_INCREMENT PRIMARY KEY,
    `content` TEXT NOT NULL COMMENT '留言内容',
    `user_id` INT NOT NULL COMMENT '留言者ID',
    `post_id` INT NOT NULL COMMENT '所属帖子ID',
    `created_at` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '留言时间',
    INDEX `idx_user` (`user_id`),
    INDEX `idx_post` (`post_id`),
    INDEX `idx_created` (`created_at`),
    CONSTRAINT `fk_messages_user` FOREIGN KEY (`user_id`) REFERENCES `users`(`id`) ON DELETE CASCADE,
    CONSTRAINT `fk_messages_post` FOREIGN KEY (`post_id`) REFERENCES `posts`(`id`) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='留言表';

-- ============================================
-- 4. 学生信息表 (students) —— 课程设计要求
-- ============================================
CREATE TABLE IF NOT EXISTS `students` (
    `xsid` INT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '学生ID',
    `XH` VARCHAR(8) NOT NULL COMMENT '学号',
    `XM` VARCHAR(20) NOT NULL COMMENT '姓名',
    `XB` ENUM('男','女') NOT NULL DEFAULT '男' COMMENT '性别',
    `CSRQ` DATE NOT NULL COMMENT '出生日期',
    `BJ` VARCHAR(6) NOT NULL COMMENT '班级',
    `ZY` VARCHAR(20) DEFAULT NULL COMMENT '专业',
    `SYD` VARCHAR(20) DEFAULT NULL COMMENT '生源地',
    `TEL` VARCHAR(11) DEFAULT NULL COMMENT '电话号码',
    `Skill` VARCHAR(50) DEFAULT NULL COMMENT '特长爱好',
    `Grjj` TEXT DEFAULT NULL COMMENT '个人简介',
    PRIMARY KEY (xsid)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='学生信息表';

